def require_columns(df: pd.DataFrame, cols: List[str]) -> List[str]:
    """Return the columns that exist in the dataframe."""
    return [c for c in cols if c in df.columns]


def safe_numeric(series: pd.Series) -> pd.Series:
    """Convert a column to numeric while keeping invalid values as NaN."""
    return pd.to_numeric(series, errors="coerce")


def money(x: float) -> str:
    """Format numeric value as US dollar text."""
    if pd.isna(x):
        return "N/A"
    return f"${x:,.0f}"


def pct(x: float) -> str:
    """Format decimal or percentage-like number."""
    if pd.isna(x):
        return "N/A"
    return f"{x:.1f}%"


def normalize_score(s: pd.Series, higher_is_better: bool = True) -> pd.Series:
    """
    Min-max normalize a numeric series into [0, 1].
    If all values are identical or missing, return 0.5.
    """
    s = pd.to_numeric(s, errors="coerce")
    if s.notna().sum() == 0:
        return pd.Series(0.5, index=s.index)

    lo, hi = s.min(), s.max()
    if np.isclose(lo, hi):
        return pd.Series(0.5, index=s.index)

    score = (s - lo) / (hi - lo)
    if not higher_is_better:
        score = 1 - score
    return score.fillna(0.5)


@st.cache_data(show_spinner=False)
def load_data() -> Tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """
    Load model output tables generated by the previous project pipeline.

    Expected main table:
    data/processed/nyc_airbnb_undervalued_model_table.csv

    Expected optional outputs:
    outputs/undervalued_candidates.csv
    outputs/undervalued_cluster_summary.csv
    outputs/model_comparison.csv
    """
    if not MODEL_TABLE_PATH.exists():
        st.error(
            "Missing model output table. Please run:\n\n"
            "`python src/04_price_prediction.py`\n"
            "`python src/05_undervalued_clustering.py`\n\n"
            f"Expected file: `{MODEL_TABLE_PATH}`"
        )
        st.stop()

    df = pd.read_csv(MODEL_TABLE_PATH, low_memory=False)

    candidates = pd.read_csv(CANDIDATE_PATH, low_memory=False) if CANDIDATE_PATH.exists() else pd.DataFrame()
    cluster_summary = (
        pd.read_csv(CLUSTER_SUMMARY_PATH, low_memory=False)
        if CLUSTER_SUMMARY_PATH.exists()
        else pd.DataFrame()
    )
    model_comparison = (
        pd.read_csv(MODEL_COMPARISON_PATH, low_memory=False)
        if MODEL_COMPARISON_PATH.exists()
        else pd.DataFrame()
    )

    return df, candidates, cluster_summary, model_comparison


def prepare_dashboard_data(df: pd.DataFrame) -> pd.DataFrame:
    """
    Add dashboard-friendly fields:
    - discount_pct: predicted-vs-actual discount percentage
    - recommendation_score: weighted ranking score for top deals
    - display_name: clean listing label
    """
    df = df.copy()

    numeric_cols = [
        "effective_price",
        "predicted_price",
        "price_gap",
        "undervaluation_ratio",
        "review_scores_rating",
        "number_of_reviews",
        "reviews_in_window",
        "distance_to_nearest_subway_km",
        "crime_intensity_log_1000m",
        "latitude",
        "longitude",
        "calendar_available_rate",
        "calendar_price_volatility",
        "weekend_price_premium",
    ]

    for col in numeric_cols:
        if col in df.columns:
            df[col] = safe_numeric(df[col])

    if "predicted_price" in df.columns and "effective_price" in df.columns:
        df["discount_pct"] = np.where(
            df["predicted_price"] > 0,
            100 * (df["predicted_price"] - df["effective_price"]) / df["predicted_price"],
            np.nan,
        )
        df["discount_pct"] = df["discount_pct"].clip(lower=0)
    else:
        df["discount_pct"] = np.nan

    # Review evidence combines lifetime reviews and study-window reviews.
    if "number_of_reviews" in df.columns:
        review_base = df["number_of_reviews"].fillna(0)
    else:
        review_base = pd.Series(0, index=df.index)

    if "reviews_in_window" in df.columns:
        review_base = review_base + 2 * df["reviews_in_window"].fillna(0)

    df["review_evidence_score_raw"] = np.log1p(review_base)

    # Rating score: use 4.0 as a reasonable lower anchor for high-quality listings.
    if "review_scores_rating" in df.columns:
        df["rating_score_raw"] = df["review_scores_rating"].fillna(df["review_scores_rating"].median())
    else:
        df["rating_score_raw"] = 4.5

    # Transit score: shorter distance to subway is better.
    if "distance_to_nearest_subway_km" in df.columns:
        df["transit_score_raw"] = df["distance_to_nearest_subway_km"]
    else:
        df["transit_score_raw"] = np.nan

    # Final recommendation score.
    # The score is intentionally interpretable:
    #   45% price discount,
    #   25% rating quality,
    #   15% review reliability,
    #   10% subway accessibility,
    #   5% lower reported-crime intensity context.
    discount_component = normalize_score(df["discount_pct"], higher_is_better=True)
    rating_component = normalize_score(df["rating_score_raw"], higher_is_better=True)
    review_component = normalize_score(df["review_evidence_score_raw"], higher_is_better=True)
    transit_component = normalize_score(df["transit_score_raw"], higher_is_better=False)

    if "crime_intensity_log_1000m" in df.columns:
        crime_component = normalize_score(df["crime_intensity_log_1000m"], higher_is_better=False)
    else:
        crime_component = pd.Series(0.5, index=df.index)

    df["recommendation_score"] = (
        45 * discount_component
        + 25 * rating_component
        + 15 * review_component
        + 10 * transit_component
        + 5 * crime_component
    )

    if "name" in df.columns:
        df["display_name"] = df["name"].fillna("Unnamed listing").astype(str).str.slice(0, 80)
    elif "id" in df.columns:
        df["display_name"] = "Listing " + df["id"].astype(str)
    else:
        df["display_name"] = "Airbnb listing"

    return df


def sidebar_filters(df: pd.DataFrame) -> pd.DataFrame:
    """Create sidebar filters and return filtered dataframe."""
    st.sidebar.header("🔎 Search Filters")

    filtered = df.copy()

    if "neighbourhood_group_cleansed" in filtered.columns:
        boroughs = sorted(filtered["neighbourhood_group_cleansed"].dropna().astype(str).unique())
        selected_boroughs = st.sidebar.multiselect(
            "Borough",
            options=boroughs,
            default=boroughs,
        )
        filtered = filtered[filtered["neighbourhood_group_cleansed"].astype(str).isin(selected_boroughs)]

    if "room_type" in filtered.columns:
        room_types = sorted(filtered["room_type"].dropna().astype(str).unique())
        selected_room_types = st.sidebar.multiselect(
            "Room Type",
            options=room_types,
            default=room_types,
        )
        filtered = filtered[filtered["room_type"].astype(str).isin(selected_room_types)]

    if "effective_price" in filtered.columns:
        max_price_available = int(np.nanpercentile(filtered["effective_price"], 95)) if filtered["effective_price"].notna().any() else 1000
        max_price = st.sidebar.slider(
            "Maximum Observed Price",
            min_value=0,
            max_value=max(100, max_price_available),
            value=max(100, max_price_available),
            step=25,
        )
        filtered = filtered[filtered["effective_price"].fillna(np.inf) <= max_price]

    min_rating = st.sidebar.slider(
        "Minimum Rating",
        min_value=0.0,
        max_value=5.0,
        value=4.5,
        step=0.1,
    )
    if "review_scores_rating" in filtered.columns:
        filtered = filtered[filtered["review_scores_rating"].fillna(0) >= min_rating]

    min_discount = st.sidebar.slider(
        "Minimum Model-Implied Discount",
        min_value=0,
        max_value=90,
        value=10,
        step=5,
        help="Discount = (predicted price - observed price) / predicted price.",
    )
    filtered = filtered[filtered["discount_pct"].fillna(0) >= min_discount]

    min_reviews = st.sidebar.slider(
        "Minimum Review Evidence",
        min_value=0,
        max_value=100,
        value=5,
        step=1,
        help="Uses lifetime number_of_reviews when available.",
    )
    if "number_of_reviews" in filtered.columns:
        filtered = filtered[filtered["number_of_reviews"].fillna(0) >= min_reviews]

    only_candidates = st.sidebar.checkbox(
        "Show only model-flagged undervalued candidates",
        value=False,
    )
    if only_candidates and "undervalued_candidate" in filtered.columns:
        filtered = filtered[filtered["undervalued_candidate"].fillna(0).astype(int) == 1]

    if "undervalued_cluster" in filtered.columns and filtered["undervalued_cluster"].notna().any():
        cluster_values = sorted(
            filtered["undervalued_cluster"]
            .dropna()
            .astype(int)
            .unique()
            .tolist()
        )
        selected_clusters = st.sidebar.multiselect(
            "Spatial Cluster ID",
            options=cluster_values,
            default=cluster_values,
        )
        filtered = filtered[
            filtered["undervalued_cluster"].isna()
            | filtered["undervalued_cluster"].astype("Int64").isin(selected_clusters)
        ]

    st.sidebar.markdown("---")
    st.sidebar.caption(
        "Tip: For a live presentation, start with all listings, then turn on "
        "'model-flagged candidates' to show how the model narrows the market."
    )

    return filtered


def metric_card(label: str, value: str, help_text: Optional[str] = None) -> None:
    """Wrapper for metric display."""
    st.metric(label=label, value=value, help=help_text)


