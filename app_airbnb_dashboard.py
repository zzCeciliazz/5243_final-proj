def render_map(filtered: pd.DataFrame) -> None:
    """Render interactive map of listings."""
    st.subheader("2. Interactive Spatial Map")

    map_cols = require_columns(
        filtered,
        [
            "latitude",
            "longitude",
            "display_name",
            "neighbourhood_group_cleansed",
            "neighbourhood_cleansed",
            "room_type",
            "effective_price",
            "predicted_price",
            "price_gap",
            "discount_pct",
            "recommendation_score",
            "review_scores_rating",
            "undervalued_candidate",
            "undervalued_cluster",
        ],
    )

    if "latitude" not in map_cols or "longitude" not in map_cols:
        st.warning("Latitude and longitude are required for the map.")
        return

    map_df = filtered[map_cols].dropna(subset=["latitude", "longitude"]).copy()

    if map_df.empty:
        st.warning("No listings available after filtering.")
        return

    # Limit map points for smoother interaction.
    map_df = map_df.sort_values("recommendation_score", ascending=False).head(3000)

    midpoint = [map_df["latitude"].mean(), map_df["longitude"].mean()]

    map_df["candidate_color"] = np.where(
        map_df.get("undervalued_candidate", 0).fillna(0).astype(int) == 1,
        255,
        90,
    )

    layer = pdk.Layer(
        "ScatterplotLayer",
        data=map_df,
        get_position="[longitude, latitude]",
        get_radius=65,
        get_fill_color="[candidate_color, 110, 80, 150]",
        pickable=True,
        auto_highlight=True,
    )

    tooltip = {
        "html": """
        <b>{display_name}</b><br/>
        Borough: {neighbourhood_group_cleansed}<br/>
        Neighborhood: {neighbourhood_cleansed}<br/>
        Room type: {room_type}<br/>
        Observed price: ${effective_price}<br/>
        Predicted price: ${predicted_price}<br/>
        Discount: {discount_pct}%<br/>
        Rating: {review_scores_rating}<br/>
        Score: {recommendation_score}
        """,
        "style": {"backgroundColor": "white", "color": "black"},
    }

    view_state = pdk.ViewState(
        latitude=midpoint[0],
        longitude=midpoint[1],
        zoom=10,
        pitch=0,
    )

    st.pydeck_chart(
        pdk.Deck(
            layers=[layer],
            initial_view_state=view_state,
            tooltip=tooltip,
            map_style="light",
        ),
        use_container_width=True,
    )

    st.caption(
        "Darker highlighted points represent stronger deal candidates under the model-based ranking. "
        "The map supports the story that undervalued opportunities are spatially uneven across NYC."
    )


